    private void cadastrar_botaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_botaoActionPerformed
        String userCampo = userName_campo.getText().trim(); //Trim retira espa√ßos em branco da String
        String passwordCampo = senha_campo.getText().trim();
        
        if (userCampo.isEmpty() && passwordCampo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        User user = new User();
        user.setUser(userCampo);
        user.setPassword(passwordCampo);
        
        try {
            UserDAO userDAO = new UserDAO();
            userDAO.cadastrarUser(user);
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar", "Cadastro", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_cadastrar_botaoActionPerformed

