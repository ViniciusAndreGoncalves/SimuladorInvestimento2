    private void entrar_botaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_entrar_botaoMouseClicked
        String userCampo = userName_campoLogin.getText().trim(); //Trim retira espa√ßos em branco da String
        String passwordCampo = password_campoLogin.getText().trim();
        
        if (userCampo.isEmpty() || passwordCampo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos", "Login", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        User user = new User();
        user.setUser(userCampo);
        user.setPassword(passwordCampo);
        
        UserDAO userDAO = new UserDAO();        
        boolean loginValido = userDAO.consultarLogin(user, userCampo, passwordCampo);

	if (loginValido) {
    	    JOptionPane.showMessageDialog(null, "Login realizado com sucesso!", "Login", JOptionPane.INFORMATION_MESSAGE);
            telaPrincipal.getSimuladorMenuItem().setEnabled(true);
            telaPrincipal.getGerenciarMenuItem().setEnabled(true);
            this.dispose();
	} else {
            JOptionPane.showMessageDialog(null, "Login inexistente\nRealize um cadastro", "Login", JOptionPane.INFORMATION_MESSAGE);
	}
        
    }//GEN-LAST:event_entrar_botaoMouseClicked

